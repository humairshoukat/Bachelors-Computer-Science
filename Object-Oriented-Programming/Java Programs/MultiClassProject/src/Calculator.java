public class Calculator {
int sum(int x, int y)
{
    return x+y;
}

int subtract(int x, int y)
{
    return x-y;
}

int multiply(int x, int y)
{
    return x*y;
}

int divide(int x, int y)
{
    return x/y;
}
}
